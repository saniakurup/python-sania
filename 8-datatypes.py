# Integer
integer_var = 42
print("Integer:", integer_var)

# Float
float_var = 3.14
print("Float:", float_var)

# String
string_var = "Hello, world!"
print("String:", string_var)

# Boolean
boolean_var = True
print("Boolean:", boolean_var)

# List
list_var = [1, 2, 3, 4, 5]
print("List:", list_var)

# Tuple
tuple_var = (1, 2, 3, 4, 5)
print("Tuple:", tuple_var)

# Set
set_var = {1, 2, 3, 4, 5}
print("Set:", set_var)

# Dictionary
dict_var = {'a': 1, 'b': 2, 'c': 3}
print("Dictionary:", dict_var)

# NoneType
none_var = None
print("None:", none_var)

# Complex
complex_var = 2 + 3j
print("Complex:", complex_var)

# Bytes
bytes_var = b"Hello"
print("Bytes:", bytes_var)

# Bytearray
bytearray_var = bytearray(b"Hello")
print("Bytearray:", bytearray_var)

# Range
range_var = range(5)
print("Range:", list(range_var))

# Frozenset
frozenset_var = frozenset({1, 2, 3, 4, 5})
print("Frozenset:", frozenset_var)

# Type of variables
print("Type of integer_var:", type(integer_var))
